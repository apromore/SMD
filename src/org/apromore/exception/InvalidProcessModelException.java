package org.apromore.exception;

/**
 * @author Chathura Ekanayake
 */
public class InvalidProcessModelException extends Exception {

	public InvalidProcessModelException() {
	}

	public InvalidProcessModelException(String arg0) {
		super(arg0);
	}

	public InvalidProcessModelException(Throwable arg0) {
		super(arg0);
	}

	public InvalidProcessModelException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
