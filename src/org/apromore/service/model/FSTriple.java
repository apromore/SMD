package org.apromore.service.model;

/**
 * @author Chathura Ekanayake
 */
public class FSTriple {

	private int first;
	private int second;
	private int third;


    public FSTriple() { }


	public int getFirst() {
		return first;
	}
	
	public void setFirst(int first) {
		this.first = first;
	}
	
	public int getSecond() {
		return second;
	}
	
	public void setSecond(int second) {
		this.second = second;
	}

	public int getThird() {
		return third;
	}

	public void setThird(int third) {
		this.third = third;
	}
}
